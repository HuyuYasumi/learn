def fact(n):
    '''
    Calculate 1*2*...*n

    >>> fact(1)
    1
    >>> fact(10)
    3628800
    >>> fact(-1)
    Traceback (most recent call last):
        ...
    ValueError
    '''
    #'>>>'的输入（即右边的表达式或函数）与输出（即下面一行）需严格遵守python交互式命令行的格式来编写

    if n < 1:
        raise ValueError()
    if n == 1:
        return 1
    return n * fact(n - 1)

